    public void scannerImel() {
        Webcam webcam = Webcam.getDefault();
        webcam.setViewSize(WebcamResolution.QVGA.getSize());

        WebcamPanel panel = new WebcamPanel(webcam);
        panel.setPreferredSize(WebcamResolution.VGA.getSize());
        panel.setFPSDisplayed(true);

        pnlWebCam.add(panel, new AbsoluteConstraints(0, 0, 280, 181));

        t1 = new Thread() {
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(100);
                    } catch (InterruptedException e) {
                    }
                    BufferedImage image = null;
                    Result result = null;
                    if (webcam.isOpen()) {
                        if ((image = webcam.getImage()) == null) {
                            continue;
                        }
                    }

                    try {
                        LuminanceSource source = new BufferedImageLuminanceSource(image);
                        BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));
                        Reader reader = new MultiFormatReader();
                        result = reader.decode(bitmap);
                    } catch (Exception e) {
                    }
                    if (result != null) {
                        JOptionPane.showMessageDialog(null, result.getText());
                        if (indexHD != -1) {
                            if (bhs.addSpSanner(result.getText(), lstHoaDon.get(indexHD).getId())) {
                                JOptionPane.showMessageDialog(null, "Th√™m s·∫£n ph·∫©m th√†nh c√¥ng");
                                fillTableGH();
                                fillTableSP();
                            } else {
                                JOptionPane.showMessageDialog(null, "S·∫£n ph·∫©m kh√¥ng t·ªìn t·∫°i");
                            }
                        }
                    }
                }
            }
        };
        t1.start();
    }

